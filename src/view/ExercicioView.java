    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        Object equipamentoSelecionado = jcbEquipamento.getSelectedItem();
        String nome = jtxNome.getText();
        String grupoMuscular = jtxGrupoMuscular.getText();

        if (equipamentoSelecionado == null || nome.isEmpty() || grupoMuscular.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!",
                "Retorno Tela", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            EquipamentoModel equipamento = (EquipamentoModel) equipamentoSelecionado;

            ExercicioModel exercicio = new ExercicioModel();
            exercicio.setIdEquipamento(equipamento);
            exercicio.setNome(nome);
            exercicio.setGrupoMuscular(grupoMuscular);

            ExercicioController controller = new ExercicioController();
            if (controller.inserir(exercicio)) {
                JOptionPane.showMessageDialog(this, "Exercício inserido com sucesso!");
                limparCampos();
                inicializa();
                preencherTabela();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao inserir o exercício!",
                    "Retorno BD", JOptionPane.ERROR_MESSAGE);
            }
        } catch (ClassCastException e) {
            JOptionPane.showMessageDialog(this, "Erro no tipo dos dados selecionados no combo.",
                "Erro de Dados", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

