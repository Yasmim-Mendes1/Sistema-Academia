    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        ExercicioModel exercicio = new ExercicioModel();

        if (jtxId_Exercicio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o código do exercício!",
                "Retorno Tela", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                exercicio.setIdExercicio(Integer.parseInt(jtxId_Exercicio.getText()));

                ExercicioController controller = new ExercicioController();
                exercicio = controller.selecionar(exercicio);

                if (exercicio == null) {
                    JOptionPane.showMessageDialog(this, "Exercício não encontrado!",
                        "Retorno BD", JOptionPane.ERROR_MESSAGE);
                } else {
                    jcbEquipamento.setSelectedItem(exercicio.getIdEquipamento());

                    jtxNome.setText(exercicio.getNome());
                    jtxGrupoMuscular.setText(exercicio.getGrupoMuscular());

                    jbNovo.setEnabled(false);
                    jbSalvar.setEnabled(false);
                    jbEditar.setEnabled(true);
                    jbExcluir.setEnabled(true);

                    jtxId_Exercicio.setEditable(false);
                    jcbEquipamento.setEnabled(true);
                    jtxNome.setEditable(true);
                    jtxGrupoMuscular.setEditable(true);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Código inválido!",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbPesquisarActionPerformed

