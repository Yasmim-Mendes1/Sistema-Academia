    private void preencherTabela() {
        ExercicioController controller = new ExercicioController();
        ArrayList<ExercicioModel> lista = controller.selecionarTodos();
        DefaultTableModel modeloTabela = (DefaultTableModel) jtExercicio.getModel();
        modeloTabela.setRowCount(0);

        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum exercício cadastrado!",
                "Retorno Tela", JOptionPane.ERROR_MESSAGE);
        } else {
            for (ExercicioModel e : lista) {
                modeloTabela.addRow(new Object[] {
                    e.getIdExercicio(),
                    e.getIdEquipamento(),
                    e.getNome(),
                    e.getGrupoMuscular()
                });
            }
        }
    }

    private void inicializa() {
        jtxId_Exercicio.setEditable(true);
        jcbEquipamento.setEnabled(false);
        jtxNome.setEditable(false);
        jtxGrupoMuscular.setEditable(false);

        jbSalvar.setEnabled(false);
        jbEditar.setEnabled(false);
        jbExcluir.setEnabled(false);
        jbPesquisar.setEnabled(true);
        jbNovo.setEnabled(true);
    }

    private void limparCampos() {
        jtxId_Exercicio.setText("");
        jcbEquipamento.setSelectedIndex(-1);
        jtxNome.setText("");
        jtxGrupoMuscular.setText("");
    }

    private void preencherComboBox() {
        EquipamentoController equipamentoController = new EquipamentoController();
        ArrayList<EquipamentoModel> listaEquipamentos = equipamentoController.selecionarTodos();

        jcbEquipamento.removeAllItems();
        for (EquipamentoModel equipamento : listaEquipamentos) {
            jcbEquipamento.addItem(equipamento);
        }
    }

